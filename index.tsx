  private _handleCommandSubmit(e: Event) {
    e.preventDefault();
    if (this.isLoading) return;
    if (this.discoveryMode === 'synthesis') {
      this.synthesizePlanetData(this.userPrompt);
    } else if (this.discoveryMode === 'chat') {
      this.handleChatSubmit();
    }
  }

